public interface NewString {
    
    /**
     * Reverses the characters in the string
     * 
     * e.g. tim would become mit
     * 
     * @return 
     */
    public String flipped();
    
    
    /**
     * Randomizes the order of the string
     * 
     * e.g. elephant may become hpentaele
     * 
     * @return 
     */
    public String randomize();
    
    
    /**
     * Returns the length of the base string
     * 
     * @return 
     */
    public int length();
    
    /**
     * Returns the string with every other letter a capital.  The first letter
     * is small
     * 
     * e.g. mississippi becomes mIsSiSsIpPi
     * 
     * @return 
     */
   // public String everyOtherCap();
    
}
